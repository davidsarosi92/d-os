/* =============================================================================
 * idt.c — IDT construction, PIC remap, and interrupt dispatch.
 *
 * Responsibilities:
 *   1. Build a 256-entry Interrupt Descriptor Table.
 *   2. Install gate descriptors for vectors 0..47 that point at the asm
 *      stubs in isr_stubs.s (each stub pushes its vector number and jumps
 *      into the common handler, which calls `isr_handler` below).
 *   3. Remap the legacy 8259 PIC so IRQ 0..15 arrive on vectors 32..47
 *      (the default mapping overlaps CPU exceptions, which is a mess).
 *   4. Provide `isr_handler` — the single C function every ISR stub funnels
 *      into — and an IRQ registration API.
 *
 * What we deliberately don't do yet: nested interrupts (handlers run with
 * interrupts disabled by IF=0 on entry; we never re-enable them inside),
 * APIC (still on legacy 8259), SMP (single-CPU assumption everywhere).
 * ============================================================================= */

#include "idt.h"
#include "hal.h"
