#include "gdt.h"
#include "syscall.h"
#include "task.h"
#include <stdint.h>

/* --------------------------------------------------------------------------
 * Gate descriptor format (Intel SDM Vol 3, §6.11).
 *
 *   offset_low   — target EIP bits 15..0
 *   selector     — code segment selector to jump through (we use kernel CS)
 *   zero         — always 0
 *   type_attr    = | P | DPL(2) | 0 | D | 1 | 1 | 0 |
 *     P   = 1 : descriptor is present
 *     DPL = 0 : only ring 0 can invoke via int N  (we lower to 3 for a
 *               future syscall vector)
 *     D   = 1 : 32-bit gate (0 would be 16-bit)
 *     low 4 bits 0b1110 = 0xE : 32-bit Interrupt Gate
 *   offset_high  — target EIP bits 31..16
 *
 * 0x8E  = P=1, DPL=0, 32-bit Interrupt Gate (the common case).
 * -------------------------------------------------------------------------- */
struct idt_entry {
    uint16_t offset_low;
    uint16_t selector;
    uint8_t  zero;
    uint8_t  type_attr;
    uint16_t offset_high;
} __attribute__((packed));

/* Pointer structure for `lidt`: limit (2) + base (4). */
struct idt_ptr {
    uint16_t limit;
    uint32_t base;
} __attribute__((packed));

#define IDT_ENTRIES 256
static struct idt_entry idt[IDT_ENTRIES];
static struct idt_ptr   idtr;

/* C-callable handler table for the 16 PIC IRQs (vectors 32..47). */
static irq_handler_t irq_handlers[16] = { 0 };

